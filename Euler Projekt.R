primzer <- function(sieb){
  ueber <- eras(sqrt(sieb))
  lis <- vector()
  for(i in 1:length(ueber)) {
    while(sieb%%ueber[i]==0){
      lis <- append(lis, ueber[i])
      sieb <- sieb/ueber[i]
    }
  }
  lis <- sort(lis, decreasing = T)
  return(lis)
}
